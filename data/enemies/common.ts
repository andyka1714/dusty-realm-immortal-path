type LateRealmThemePath = "sword" | "body" | "mage";

const getLateRealmThemePool = (
  set: string[],
  path: LateRealmThemePath
) => {
  switch (path) {
    case "sword":
      return set.slice(0, 6);
    case "body":
      return set.slice(6, 12);
    case "mage":
      return set.slice(12, 18);
  }
};

const mergeDropPools = (...pools: string[][]) =>
  Array.from(new Set(pools.flat()));

const createDualThemePool = (
  set: string[],
  left: LateRealmThemePath,
  right: LateRealmThemePath
) =>
  mergeDropPools(
    getLateRealmThemePool(set, left),
    getLateRealmThemePool(set, right)
  );

const createTrifoldThemePool = (set: string[]) =>
  mergeDropPools(
    [set[0], set[1], set[2], set[4]],
    [set[6], set[7], set[8], set[10]],
    [set[12], set[13], set[14], set[16]]
  );

